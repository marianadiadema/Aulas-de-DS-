package cap03; //cria o pacote java
public class ContadorProgressivoFor { //abre a classe pública ContadorProgressivoFor
    public static void main(String args[]) { //abre o método principal da classe
        for (int i = 0; i < 10; i++) { //faz uma variável dentro de um método de repetição
            System.out.println(i + " "); //exibe na tela o resultado da operação
        }
        System.out.println("\nAcabou!"); //mostra na tela uma mensagem de "despedida"
    }
}
